import math

num=int(input("enter a number:"))

#common functions in math module
print(math.sqrt(num))
print(math.floor(num))
print(math.pow(num,2))
print(math.factorial(num))
print(math.log(num))

#constants in math module
print(math.pi)
print(math.e)

#trignometric functions in math module
print(math.sin(num))
print(math.cos(num))
print(math.tan(num))
print(math.acos(num))
print(math.atan(num))